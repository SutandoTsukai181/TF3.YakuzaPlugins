            var poWriter = new ExtractStrings();
            Po po = poWriter.Convert(karuta);

            po.Entries[0].Translated = "Translation test";
            var translator = new Translate();
            translator.Initialize(po);

            KarutaFile karutaResult = translator.Convert(karuta);

            Assert.AreEqual("Translation test", karutaResult.Cards[0].Text);
        }

