        [Test]
        public void ReadKaruta()
        {
            byte[] data = new byte[_data.Length];

            Array.Copy(_data, data, _data.Length);

            using DataStream ds = DataStreamFactory.FromArray(data, 0, data.Length);
            BinaryFormat binary = new BinaryFormat(ds);

            var converter = new Reader();

            KarutaFile karuta = converter.Convert(binary);

            Assert.IsNotNull(karuta);
            Assert.AreEqual(48, karuta.Cards.Length);

            KarutaCard card = karuta.Cards[0];

            Assert.IsNotNull(card);
            Assert.AreEqual(0, card.Index);
            Assert.AreEqual(101, card.IndexOther);
            Assert.AreEqual("", card.Text);
        }

